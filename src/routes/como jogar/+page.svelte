 <h1>  COMO JOGAR O QUEBRA-CABEÇA </h1>
 
 <br/>

<h3> 1. INÍCIO DO JOGO: </h3>

<p> 
    • Ao iniciar o jogo, você verá uma imagem dividida em várias peças misturadas. 
</p>

<p>
    • O objetivo é reorganizar as peças para reconstruir a imagem original.

<h3>  2. AO CLICAR E SELECIONAR, AS PEÇAS SERÃO MOVIDAS: </h3>

<p>
    • Clique em uma peça para selecioná-la e escolha onde deseja colocá-la. </p>

<p>
    • Continue clicando para mover as peças até que todas estejam no lugar correto. 

<p> • Assim que todas as peças da imagem estiverem no lugar correto, você passará para o próximo nível. </p>

<h3>  4. FINALIZANDO O JOGO: </h3>

<p>
    • O jogo termina quando todas as peças estão na posição correta, formando a imagem completa.
</p>

